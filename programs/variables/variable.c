#include <stdio.h>
int main(){
    int a = 10;   //Integer
    float b = 25.2;  //Floating point
    double c = 10.034;  //Double
    char d = 'd';
    
    printf("Integer variable a: %d\n", a);
    printf("Float variable b: %f\n", b);
    printf("Integer variable c: %lf\n", c);
    printf("Integer variable d: %c\n", d);    
}
